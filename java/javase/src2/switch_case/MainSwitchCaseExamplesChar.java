package switch_case;

public class MainSwitchCaseExamplesChar {

	public static void main(String[] args) {
		
		char c = 'A';

		
		switch(c) {
		case 'A':{System.out.println("ela");} break;
		case 'B':{System.out.println("yaxsi");} break;
		case 'C':{System.out.println("kafi");} break;
		case 'D':{System.out.println("pis");} break;
		default:{System.out.println("duz qiymet verilmeyib");}
		}
	}
}
