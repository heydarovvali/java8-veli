package switch_case;

public class MainSwitchCaseExamplesString {

	public static void main(String[] args) {
		
		String name = "Hesen";

		
		switch(name) {
		case "Adil":{System.out.println("ela");} break;
		case "Cavid":{System.out.println("yaxsi");} break;
		case "Ayxan":{System.out.println("kafi");} break;
		case "Cemil":{System.out.println("pis");} break;
		default:{System.out.println("duz qiymet verilmeyib");}
		}
	}
}
