package assignment_operators;

public class AssignmentOperators {

	public static void main(String[] args) {

		int a = 12;
		int b = 7;
		int c = 0;
		c = a + b;
		System.out.println(c);
		c = a - b;
		System.out.println(c);
		c = a * b;
		System.out.println(c);
		c = a / b;
		System.out.println(c);
		c = a % b;
		System.out.println(c);
	}

}
