
public class VariablesPracticeHome {
	public static void main(String[] args) {
		int age = 25;
		System.out.println(age);
		System.out.println("age : 25");
		
		boolean hasJob = false;
		System.out.println(hasJob);
		System.out.println("hasJob : false");
		
		char studentGrade = 'C';
		System.out.println(studentGrade);
		System.out.println("studentGrade : C");
		
		double carPrice = 25000;
		System.out.println(carPrice);
		System.out.println("carPrice : 25000");
		
		String employeeName = "Malik";
		System.out.println("employeeName Malik");
		
		
	
	}

}
